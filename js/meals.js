document.getElementById("drop_down_ingri").addEventListener('click', function(){
    var box1 = document.getElementById('drop_it')
    if(box1.style.display=="none")
    {
        box1.style.display = "block"
    }
    else
    {
        box1.style.display = "none"
    }
})

